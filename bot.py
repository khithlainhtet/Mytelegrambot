async def start(update: Update, context: ContextTypes.DEFAULT_TYPE):
    await update.message.reply_text("မင်္ဂလာပါ! Shwe mm ​ဗေဒင် မှာကြိုဆိုပါတယ်။\n/readmyfortune လို command နဲ့ သင့်ကံကြမ္မာစာကို ရယူပါ။")

async def readmyfortune(update: Update, context: ContextTypes.DEFAULT_TYPE):
    fortune = random.choice(fortunes)
    await update.message.reply_text(fortune)

async def help_command(update: Update, context: ContextTypes.DEFAULT_TYPE):
    await update.message.reply_text("Commands available:\n/start - စတင်ရန်\n/readmyfortune - ကံကြမ္မာစာဖတ်ရန်")

def main():
    app = ApplicationBuilder().token(TOKEN).build()

    app.add_handler(CommandHandler("start", start))
    app.add_handler(CommandHandler("readmyfortune", readmyfortune))
    app.add_handler(CommandHandler("help", help_command))

    print("Sayargyi Hanthar Bot is running...")
    app.run_polling()

if __name__ == "__main__":
    main()
